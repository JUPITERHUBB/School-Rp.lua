local player = game:GetService("Players").LocalPlayer
local gui = Instance.new("ScreenGui", player.PlayerGui)
gui.Name = "FriteOnTopLoader"
gui.ResetOnSpawn = false
gui.IgnoreGuiInset = true 
gui.ZIndexBehavior = Enum.ZIndexBehavior.Sibling 
gui.DisplayOrder = 9999 

local background = Instance.new("Frame", gui)
background.Size = UDim2.new(1, 0, 1, 0)
background.Position = UDim2.new(0, 0, 0, 0)
background.BackgroundColor3 = Color3.new(0, 0, 0)
background.BorderSizePixel = 0
background.ZIndex = 1

local textLabel = Instance.new("TextLabel", background)
textLabel.AnchorPoint = Vector2.new(0.5, 0.5)
textLabel.Size = UDim2.new(0, 600, 0, 120)
textLabel.Position = UDim2.new(0.5, 0, 0.5, 0)
textLabel.Text = "FRITE ON TOP"
textLabel.Font = Enum.Font.GothamBlack
textLabel.TextSize = 72
textLabel.TextStrokeTransparency = 0.5
textLabel.TextStrokeColor3 = Color3.new(0, 0, 0)
textLabel.BackgroundTransparency = 1
textLabel.ZIndex = 2

local hue = 0
local dotCount = 0
local lastUpdate = tick()

game:GetService("RunService").RenderStepped:Connect(function()

    hue = (hue + 0.005) % 1
    textLabel.TextColor3 = Color3.fromHSV(hue, 1, 1)
    
    if tick() - lastUpdate >= 0.5 then
        dotCount = (dotCount % 1) + 1
        textLabel.Text = "FRITE ON TOP" .. string.rep(" No More Script", dotCount)
        lastUpdate = tick()
    end
end)
